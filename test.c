#define _CRT_SECURE_NO_WARNINGS 1

#include <stdio.h>

// 学习指针第1天

int main()
{
	int a = 10;
	int* p = &a;
	printf("%d\n", a);
	/*
		如何理解指针变量？
		*是在说明p是指针变量
		p说明的是指向的对象是int类型
		
		指针变量就是用来存放地址的！！
		存放在指针变量中的值，都会被当成地址使用！！	
	*/

	*p = 0;//* 解引用操作符（间接引用操作符）
	//*p的意思是通过p中存放的地址，找到地址指向的空间，*p其实就是a
	printf("%d\n", a);
	

	return 0;
}


